    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //Button Login in Panel Login and check access level
        if(txtUsername.getText() == null){
            JOptionPane.showMessageDialog(null, "Please fill in Username");
        }else if(txtPassword.getText() == null){
            JOptionPane.showMessageDialog(null, "Please fill in Password");
        }else{
            try {
                //disini buat loginnya
                iLogin.setUsername(txtUsername.getText());
                iLogin.setPassword(txtPassword.getText());
                
                int cek = iLogin.doLogin();//rs.getInt("hasil");
                if(cek > 0){
                    String[] data = iLogin.cekAccess();
                    LoginHelper.idUser = data[0];
                    LoginHelper.uName = data[1];
                    LoginHelper.isAdmin = data[2];
                    if(LoginHelper.isAdmin.equals("1")){
                        JOptionPane.showMessageDialog(null, "Welcome, " + txtUsername.getText() + ". \n Have a nice day:)");
                        btnDoctor.setEnabled(true);
                        btnPatient.setEnabled(true);
                        btnTreatment.setEnabled(true);
                        btnRecipe.setEnabled(true);
                        btnPayment.setEnabled(true);
                        btnDrug.setEnabled(true);
                        btnSpecialist.setEnabled(true);
                        btnUser.setEnabled(true);
                        JMWelcome.setVisible(true);
                        JMWelcome.setText("Welcome: " + txtUsername.getText());
                        JMILogin.setText("Logout");
                        LoginPanel.setVisible(false);
                        txtUsername.setText(null);
                        txtPassword.setText(null);                      
                        
                    }else if(LoginHelper.isAdmin.equals("0")){
                        JOptionPane.showMessageDialog(null, "Welcome, " + txtUsername.getText() + ". \n Have a nice day:)");
                        btnDoctor.setEnabled(false);
                        btnPatient.setEnabled(true);
                        btnTreatment.setEnabled(true);
                        btnRecipe.setEnabled(true);
                        btnPayment.setEnabled(true);
                        btnDrug.setEnabled(false);
                        btnSpecialist.setEnabled(false);
                        btnUser.setEnabled(false);
                        JMWelcome.setVisible(true);
                        JMWelcome.setText("Welcome: " + txtUsername.getText());
                        JMILogin.setText("Logout");
                        LoginPanel.setVisible(false);
                        txtUsername.setText(null);
                        txtPassword.setText(null);
                    }
                }else if((cek == 0)){
                    JOptionPane.showMessageDialog(null, "Incorrect username or password or login level not accordance.");
                    JMWelcome.setEnabled(false);
                    JMWelcome.setVisible(false);
                    JMILogin.setText("Login");
                }
            } catch (Exception e) {
                System.out.println("Error " + e);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

