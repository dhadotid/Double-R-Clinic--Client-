    private void JMILoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JMILoginActionPerformed
        //Button Login and Logout
        if(JMILogin.getText().trim().equals("Logout")){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure want to logout?", "Logout", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                
                JMWelcome.setEnabled(false);
                JMWelcome.setVisible(false);
                btnDoctor.setEnabled(false);
                btnPatient.setEnabled(false);
                btnTreatment.setEnabled(false);
                btnRecipe.setEnabled(false);
                btnPayment.setEnabled(false);
                btnDrug.setEnabled(false);
                btnUser.setEnabled(false);
                btnSpecialist.setEnabled(false);
                JMILogin.setText("Login");
                LoginPanel.setVisible(false);
            }
            
        }else if(JMILogin.getText().trim().equals("Login")){
            LoginPanel.setVisible(true);
            txtUsername.requestFocus();
            //txtUsername.grabFocus();
        }
    }//GEN-LAST:event_JMILoginActionPerformed

