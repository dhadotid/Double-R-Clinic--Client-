    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //delete
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure want to Delete?" + txtDrugName.getText(), "Delete", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            try {
                inDrug.setDrugID(txtIDD.getText());
                int res = inDrug.doDelete();
                if(res > 0){
                    JOptionPane.showMessageDialog(null, "Success delete data " + txtDrugName.getText());
                    tableload();
                    clearall();
                    autoid();
                }
            } catch (Exception e) {
                System.out.println("Error: " + e);
            }
        }
        }

