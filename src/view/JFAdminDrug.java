    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String exp = new SimpleDateFormat("yyyy-MM-dd").format(dcExpDate.getDate());
        if (txtIDD.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in Id Drug");
        }else if(txtDrugName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill in Drug Name");
            txtDrugName.requestFocus();
        }else if(cbDrugtype.getSelectedItem().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter a valid Drug Type");
            cbDrugtype.requestFocus();
        }else if(txtStock.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter a valid Stock");
        }else if(dcExpDate.equals("") || exp.compareTo(datenow) < 0){
            JOptionPane.showMessageDialog(null, "Please fill in data ExpDate correctly");
            dcExpDate.requestFocus();
            System.out.println("Input: " + exp);
            System.out.println("Datenow: " + datenow);
        }else if(txtPrice.getText().matches("^\\d+$") == false || txtPrice.getText().equals("") || txtPrice.getText().length() < 2){
            JOptionPane.showMessageDialog(null, "Inccorect format price");
        }else{
            if(txtIDD.getText().equals("full")){
                JOptionPane.showConfirmDialog(null, "Data Drug has been full");
            }try{
                inDrug.setDrugName(txtDrugName.getText());
                inDrug.setDrugType(cbDrugtype.getSelectedItem().toString());
                inDrug.setDrugStock(Integer.parseInt(txtStock.getText()));
                inDrug.setDrugEXP(exp);
                inDrug.setDrugPrice(Double.parseDouble(txtPrice.getText()));

                int i = inDrug.doInsert();
                if(i != 0){
                    JOptionPane.showMessageDialog(null, "Data drug successful inputted");
                    autoid();
                    tableload();
                    txtPrice.setText("");
                    txtDrugName.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "Data drug failed inputted");
                }
            } catch (Exception e) {
                System.out.println("Error: " + e);
            }
        }

    }//GEN-LAST:event_btnInsertActionPerformed

